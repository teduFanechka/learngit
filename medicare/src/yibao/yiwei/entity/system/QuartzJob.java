package yibao.yiwei.entity.system;

/**
 * quartz定时任务
 * @author Administrator
 *
 */
@SuppressWarnings("serial")
public class QuartzJob implements java.io.Serializable {
	
	private String trigger_name;
	private String job_name;
	private String job_group; //分组
	private String trigger_state;//状态̬
	private String cron_expression; //表达式
	private String description; //详情 
	private String className;//类
	
	public QuartzJob() {
		super();
	}

	public String getTrigger_name() {
		return trigger_name;
	}

	public void setTrigger_name(String trigger_name) {
		this.trigger_name = trigger_name;
	}

	public String getJob_name() {
		return job_name;
	}

	public void setJob_name(String job_name) {
		this.job_name = job_name;
	}

	public String getJob_group() {
		return job_group;
	}

	public void setJob_group(String job_group) {
		this.job_group = job_group;
	}

	public String getTrigger_state() {
		return trigger_state;
	}

	public void setTrigger_state(String trigger_state) {
		this.trigger_state = trigger_state;
	}

	public String getCron_expression() {
		return cron_expression;
	}

	public void setCron_expression(String cron_expression) {
		this.cron_expression = cron_expression;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

}
